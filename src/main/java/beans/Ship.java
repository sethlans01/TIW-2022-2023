package beans;

public class Ship {
	
	private String min;
	private String max;
	private String cost;
	private String gratis;
	private String minGrat;
	
	public String getMin() {
		return min;
	}
	public void setMin(String min) {
		this.min = min;
	}
	public String getMax() {
		return max;
	}
	public void setMax(String max) {
		this.max = max;
	}
	public String getGratis() {
		return gratis;
	}
	public void setGratis(String gratis) {
		this.gratis = gratis;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getMinGrat() {
		return minGrat;
	}
	public void setMinGrat(String minGrat) {
		this.minGrat = minGrat;
	}
	
}